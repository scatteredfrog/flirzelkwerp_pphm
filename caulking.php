          <tr><td style="padding-left: 15px;">
                  <h3>Caulking 101</h3>
                  <dl><dt>Materials</dt>
                  <ul>
                      <li id="nonMenu">Razor blade or painter's 5-in-1 tool to remove old caulk</li>
                      <li id="nonMenu">Dripless caulk gun (worth buying if you're not sure whether your caulk gun is dripless)</li>
                      <li id="nonMenu">Clean rag</li>
                      <li id="nonMenu">Old magazine or Sunday section of newspaper (something with shiny paper)</li>
                      <li id="nonMenu">Toilet paper</li>
                      <li id="nonMenu">Small dish or saucer with liquid soap (to break the surface tension of the caulk)</li>
                      <li id="nonMenu">Silicone tub and tile caulk -- GE Silicone I (available at the <a target="_blank" href="http://ww3.truevalue.com/smolka/Home.aspx">
                              True Value at 4305 N. Milwaukee Ave.</a>) is the absolute easiest to work with. GE Silicone II will also work.
                      </li>
                      <li id="nonMenu">Caulk Rite caulking tool -- this inexpensive (approx. $5) tool is the secret to a good caulk job. It's 
                      hand-held and has a triangle that acts like a Squeegee.</li>
                  </ul>&nbsp;<br />
          <dt>
              Preparation</dt>
              <ul>
                  <li id="nonMenu">Remove all of the old caulking and any other loose debris. (This is the 
                  toughest part of a caulking job.)</li>
                  <li id="nonMenu">Treat the open area with a 10% to 20% bleach solution to kill any mold; 
                      use a rag.</li>
                  <li id="nonMenu">Wipe clean of any debris. Vacuum the crack if your vacuum cleaner 
                  has a hose attachment.</li>
              </ul>&nbsp;<br />
          <dt>
              Technique</dt>
              <ul>
                  <li id="nonMenu">Open the magazine and place it in the tub. Have a dish of soap, the Caulk 
                  Rite tool, and toilet paper handy.</li>
                  <li id="nonMenu">Open the caulk and practice with scraps of wood or metal shaped like a corner. 
                      The faster you go with the caulk gun, the more even the caulk will be. Holding the Caulk 
                      Rite tool perpendicular will give you a small bead; holding the tool lower to the tub will 
                      give you a fatter bead (and cover larger defects if there are any).</li>
                  <li id="nonMenu">Caulk the joint by pulling from a corner to the middle and forcing caulk into 
                      any space under the tiles.</li>
                  <li id="nonMenu">Put your caulk gun down on the magazine.</li>
                  <li id="nonMenu">Dip the Caulk Rite tool in soap; this will make the tool slide easier.</li>
                  <li id="nonMenu">Put the tool in the corner and pull toward the center of the wall. At the end 
                      of this stroke, you will have a gob of caulk on the end of the tool. Wipe it on the magazine;
                      when the magazine is full of caulk, turn the page.</li>
                  <li id="nonMenu">Use toilet paper to wipe the tip of the caulk gun and Caulk Rite tool.</li>
                  <li id="nonMenu">Caulk the joint from the other corner toward the center, using less pressure as you 
                      close in.</li>
                  <li id="nonMenu">Wipe the gob of caulk on magazine.</li>
                  <li id="nonMenu">Repeat the above steps as necessary.</li>
                  <li id="nonMenu">Let the fresh caulk cure for 24 hours before exposing it to water.</li>
               </ul>&nbsp;<br />
               <dt>Good luck with your project!</dt></dl>
