                  <h3>Preventive Maintenance</h3>

                  <dl>             
                      <dt>Sink Leaks</dt>
                      The J- or S-shaped tube under your sink is called a <i>P-trap</i>.  Its purpose is to 
                      fill with water, make a seal, and prevent sewer gas from coming into the home. Drain 
                      pipes can be metal or plastic.
                      <br />&nbsp;<br />
                      The only advantage of a metal drain pipe is that it looks better than plastic. Plastic 
                      drain pipes are a better choice than metal drain pipes because:
                      <ul style="margin:5px 12px; padding:5px 12px;">
                          <li style="width:525px;">They do not corrode (ever)</li>
                          <li style="width:525px;">They flow better. (Things have a much harder time getting stuck in plastic pipes.)</li>
                          <li style="width:525px;">They rarely leak.</li>
                          <li style="width:525px;">They last forever.</li>
                      </ul><br />
                      I receive a call to replace a corroded (and/or leaking) P-trap every week or two. The 
                      metal (chrome-plated brass) P-trap in my vanity took 20 years to fail. But metal P-traps 
                      <i>will</i> fail; the only question is <i>when</i> they will fail. I've never been called for 
                      problems with a properly-installed <i>plastic</i> P-trap, and I've never seen one corroded or 
                      ready to fail.
                      <br />&nbsp;<br />
                      There are two tests to see if your sink is going to leak:
                      <ol>
                          <li>Touch (as delicately as possible) the bottom of the metal drain under your sink. If it 
                          feels like sandpaper, then the drain is close to the end of its useful life.</li>
                          <li>Gently push on the bottom of the pipe. If there is any movement at all, the pipe is 
                          in danger of imminent failure. REPLACE IT SOON!!</li>
                      </ol><br />
                      <i>Never</i> do test #2:
                      <ul>
                          <li>before dinner</li>
                          <li>if you have anything else to do that day</li>
                          <li>if you didn't get--or don't need--a good night's sleep</li>
                          <li>when the plumbing stores are closed or about to close</li>
                      </ul>
                      <br />&nbsp;<br />
                      <dt>Bare Wood</dt>
                      <i>All exterior tissue</i> (including bare wood) should have a quick debridement (a visit from 
                      the scraper or a wire brush) and a dressing (spot primer) before the winter. This is essential 
                      for the health of your home!!
              </dl>
