                        <p>
                        There was a golf ball-sized hole where the toilet supply line came through the wall. The hole 
                        previously had been covered by a large vintage baseboard that the installers had removed and 
                        substituted with small contemporary door casings (moldings) less than half the size of the 
                        originals, and quarter round for base shoe. I measured the distance from the wall to the 
                        flange bolt removing one of the caps. More than half the threads inside the nut were visible. 
                        The nut couldn't have been on two whole turns.</p>
                        <p>
                        Chapter 1 of the handyman text warns that the "OMG" phrase does not bring comfort to your 
                        customer. Nonetheless, I let it slip out for the first time in the ten years I've been in 
                        business. Loose bolts and a &frac14;-inch space between the side of the toilet and the 
                        floor...</p>
