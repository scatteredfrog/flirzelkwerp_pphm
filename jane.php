                        <p>In summary, what they did to Jane's bathroom was:</p>
                        <ul>
                            <li>Remove vintage baseboard and replace it with incorrectly sized modern door trim</li>
                            <li>Remove vintage base shoe and replace it with quarter round (which sticks out 
                                farther from the wall)</li>
                            <li>Leave a golf ball-sized hole around the toilet's supply line (who knows what 
                                may crawl out!)</li>
                            <li>Not Spackle the nail holes</li>
                            <li>Leave a &frac14;-inch space between the toilet and the floor</li>
                            <li>Install a floor but not finish the job</li>
                            <li>Install a bathroom but no bathroom door</li>
                            <li>Install a medicine cabinet that was loose within a week and certain to fall</li>
                            <li>Charge her an additional fee to reinstall the correct toilet</li>
                        </ul></p>
                        <p>
                        I sent Jane a copy before posting this, and below is her reply:
                        <blockquote><p>
                            Hi, Rich --</p>
                            <p>
                            Much thanks for re-hanging the medicine cabinet and for everything.</p>
                            <p>
                            Please change my name in the editorial. We quiet, cat-loving women like deep cover :o)</p>
                        </blockquote><p>
                        After Thanksgiving, I stopped at the store again and spoke with a fourth manager and gave him 
                        a copy of this letter before launching a "Campaign for Justice" on YouTube.</p>
                        <p>
                        About a week later, I received an e-mail from Jane telling me that the company agreed to issue 
                        a refund of $360.00 that she accepted.</p>
                        <p>
                        Someday a guest may bump that toilet and suddenly the neighbor below has a new water feature 
                        in the ceiling. For $360, the store got off cheap, but Jane got more of a refund than she 
                        expected.</p>
                        <p>
                        Installations by the big box stores come with a hidden cost. Their installation charges are 
                        low and are split at least three ways:
                        <ul>
                            <li>The big box store takes a cut.</li>
                            <li>The company that hires the installer takes its cut.</li>
                            <li>The guy doing the work gets whatever money is left over.</li>
                        </ul>
                        </p>
                        <p>
                        This built-in incentive to finish and get to the next job may not allow the best quality 
                        for your installation.</p>
                        
