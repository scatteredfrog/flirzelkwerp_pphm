                    <td><h3>Jane's Story</h3><p>
                        Jane is a quiet, cat-loving woman with many large books in her home. It wouldn't surprise 
                        me if she has read -- and understood -- almost all of those tomes. Jane isn't loud, 
                        abrasive, confrontational, or in any other way unpleasant; she also has fewer gray hairs  
                        than I do. Jane called me in mid-October 2012. She needed a door to be cut down and a 
                        threshold installed.</p>
                        <p>
                        Jane had hired someone to remodel her bathroom. The remodeler installed a tile floor, and
                        <ul>
                            <li>left the raw edge of tile and globs of adhesive tapes exposed, and</li>
                            <li>because of the thickness of the tile, the bathroom door couldn't be put back on.</li>
                        </ul>
                        </p>
                        <p>
                        A company, previously thought to be reputable, sold Jane a new bathroom sink, toilet, 
                        medicine cabinet, and tile floor. They sent someone to take measurements and arranged the 
                        installation. The crew that installed the tile didn't tell Jane that she would need a 
                        threshold (transition strip), nor could they shave the door to refit it in the hinges, 
                        bringing up some curious questions:<ul>
                            <li>Someone who makes a living remodeling bathrooms doesn't know that a transition 
                                strip is necessary after setting ceramic tile on an existing floor?</li>
                            <li>Someone who makes a living installing flooring can't install a transition strip?</li>
                            <li>Someone who makes a living installing tile isn't able to shave a door?</li>
                            <li>Who would let a customer suddenly go without a bathroom door?</li>
                        </ul></p><p>
                        The company Jane hired should not have left her with an unfinished floor and without a 
                        bathroom door. I offered to talk to a company representative on her behalf, but Jane 
                        didn't want to go there. The installation, scheduling, and, well, the whole experience 
                        had been unpleasant. She wanted to put it all behind her and move on. I remember wishing 
                        I could be that strong (or that smart).</p>
                        <p>
                        For the transition strip, I purchased two moldings, which I combined, trimmed to fit, 
                        stained, and installed. I shaved the door, sealed the bottom of it to prevent warping, 
                        and put it back on its hinges.</p>
                        <p>
                        Unfortunately, the door would not close because the new toilet's elongated bowl was in the 
                        way. Jane called the aforementioned company's manager, who offered to sell her a different 
                        toilet and only charge her half for the repeat installation. Jane let me talk to the manager, 
                        but he didn't want to talk to me. (Can't blame him.) I finished the other things on Jane's 
                        to-do list and met a friend for lunch. The plan was for me to return to hang the door and 
                        some new towel bars after the toilet was changed.</p>
                        <p>
                        The store Jane was originally dealing with was on my way home, so I offered to stop and
                        personally speak with the manager. Jane agreed and gave me a piece of paper with her phone 
                        number, purchase order number, and written permission to allow me to speak on her behalf.
                        When I arrived, a different manager -- the afternoon manager -- was on duty. He listened to
                        the details and agreed that a customer who purchased a bathroom remodel should not be left
                        with ragged exposed tiles, and should have a bathroom door that would close. He said he'd 
                        contact the installation department and "get back to me." (He didn't.)
                        </p>
                        <p>
                        The store changed the toilet (and charged $200), and I went back to Jane's to install a 
                        towel bar, toilet paper holder, switch cover plate, and a knob for the medicine cabinet.</p>
                        <p>
                        While installing the knob, I found that the medicine cabinet was loose. There were two 
                        &frac14;-inch spaces between the cabinet and the wall. The wall was not bowed. The cabinet
                        had been mounted with four drywall screws. No toggles, no screws into studs, no wall 
                        anchors, no shims; just four screws into drywall. I removed and reinstalled the cabinet 
                        using four wall anchors and two screws into studs.
                        </p>
