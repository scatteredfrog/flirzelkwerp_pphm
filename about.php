      <h3><!-- #BeginEditable "Title" -->The Portage Park Handyman<!-- #EndEditable --></h3>
      <!-- #BeginEditable "Paragraph" -->
      <p>Rich Escallier, a Portage Park resident since 1988 and home handyman 
        since 1978, offers a large range of indoor/ outdoor repairs and improvements. 
        Because he is a self-employed, independent handyman, he can perform services 
        at prices far below those of franchised handyman services. Rich brings 
        over 25 years of experience in the home repair field. As a former owner 
        of several apartment buildings, he is largely self-taught, learning the 
        ins and outs of repairing and refurbishing buildings since 1978.</p>
      <p>Rich has taught home repair and maintenance classes since 2006 as part of
      adult continuing education programs at the 
      <a href="http://ccc.edu" target="_new">City Colleges of Chicago</a>. Watch the 
      <a href="http://ccc.edu/colleges/truman" target="_new">Truman College</a> course catalog for 
      his upcoming classes beginning in Summer 2012.</p>
      <p> His most extensive remodeling job was completed on his current residence, 
        a 90- year-old Portage Park home which he refurbished from top to bottom, 
        including building a second story addition. </p>
