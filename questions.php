        You need to ask certain questions to make sure you're comparing apples 
        to apples when shopping for a handyman:
        <ul style="width: 90%; list-style-type: disc; margin-left: 25px; margin-top: 15px;">
            <li class="q">How long have you been in the handyman business?</li>
            <li class="q">How long have you been in the handyman business full-time?</li>
            <li class="q">Are you certified, licensed, and/or insured?</li>
            <li class="q">Do you require money up front or do I pay when I am satisfied with the work?</li>
            <li class="q">Do you guarantee or warranty your work?</li>
            <li class="q">Do you have formal training?</li>
            <li class="q">Do you mark up your supplies?</li>
            <li class="q">What vehicle to you use (family sedan, or dedicated work truck/van?)</li>
            <li class="q">Do you enjoy working on your own home? (translation: do you really enjoy your job?)</li>
            <li class="q">What kind of toolbox do you show up with? (in other words, how prepared are you?)</li>
            <li class="q">What percentage of your visits involves a trip to the store for supplies?</li>
            <li class="q">What percentage of your visits involves a second trip to the store? (and, will I be charged for a second trip to the store?)</li>
            <li class="q">How do I know I can trust you in my house?</li>
            <li class="q">Have you ever (or, when was the last time you) missed an appointment?</li>
            <li class="q">What happens we are just "not working right together"?</li>
            <li class="q">Are you allergic to anything with four legs (if applicable)?</li>
            <li class="q">If I think of something that wasn't on my list, what are the chances you can take care of it in the same visit?</li>
            <li class="q">Are you comfortable working in my home when I'm not there?</li>
            <li class="q">Do you work/come alone?</li>
            <li class="q">Do you schedule smaller jobs geographically?</li>
            <li class="q">Do you offer weekend or off-hour services? emergency services for existing customers (when possible)?</li>
            <li class="q">How long have you been fixing things? (Jim Mullen's radio)</li>
            <li class="q">Do you like your job?</li>
