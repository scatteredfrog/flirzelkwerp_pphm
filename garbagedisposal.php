                  <h3>Garbage Disposal DIY</h3>
                  <dl><dt>General Tips</dt>
                  <ul>
                      <li id="nonMenu">A garbage disposal is not a compost pile. To dispose of a whole 
                          salad, or something else organic, put it in a compost pile. If 
                          you want to rinse a couple of scraps off your plate, that's ok.</li>
                      <li id="nonMenu">Feed your disposal a couple of ice cubes every once in a while. 
                          The abrasive action of an ice cube caroming around inside the 
                          chamber knocks crud (technical term!) loose.</li>
                      <li id="nonMenu">Feed your disposal lemon, orange or grapefruit peels occasionally. 
                          The citrus will help disinfect things.</li>
                      <li id="nonMenu">Lemon juice ice cubes will accomplish both disinfecting and 
                          "knocking crud" loose.</li>
                      <li id="nonMenu">Run the garbage disposal using hot water before starting the 
                          dishwasher. This does two things. First, it brings hot water from 
                          the heater to the kitchen so the dishwasher will start with hotter 
                          water (and clean the dishes better). Second, it lets the 
                          dishwasher drain into an unobstructed disposal. (If the dishwasher 
                          has its own trap and does not empty into the disposal, this tip 
                          doesn't apply.)</li>
                      <li id="nonMenu">Statistics show that more service calls about garbage disposal problems 
                          come from homes with little boys than from homes without little boys.</li>
                      <li id="nonMenu">Many service calls come after a party where 'lubricated' friends help 
                          clean things up, not just with garbage disposals but also dishwashers. 
                          If you let your friends load the dishwasher, check it before you run it.</li>
                      <li id="nonMenu">Do not wash long-haired pets or allow long-haired people to wash their hair 
                          in the sink without a strainer.</li>
                      <li id="nonMenu">If the disposal stops working and crud is allowed to cake on the mechanics, 
                          the blades can freeze up. A chisel may or may not free the blades.</li>
                      <li id="nonMenu">You can buy a box of disposal cleaning packets. I don't know how useful or 
                          necessary these packets are, but it is pretty fun to watch the blue foamy 
                          volcano while going through the process.</li>
                      <li id="nonMenu">Use a strong magnet to attach a hex key (which came with your disposal) to 
                          the ring that connects the disposal to the sink. This way it will be where 
                          you need it when you need it.</li>
                  </ul>&nbsp;<br />
          <dt>
              Garbage disposals do not like:</dt>
              <ul>
                  <li id="nonMenu">Stringy things (hair, celery, cigarette butts, etc.)</li>
                  <li id="nonMenu">Things that expand - for example, rice starts out small, but gets larger; also,
                      sticky rice can clog a disposal or drain more than most other food products.</li>
                  <li id="nonMenu">Metal, glass, or hard plastic</li>
                  <li id="nonMenu">Gooey things like molasses, melted chocolate, melted cheese (such as from fondue)</li>
              </ul>&nbsp;<br />
          <dt>
              Troubleshooting</dt>
              <ul>
                  <li id="nonMenu">NEVER put any of your favorite fingers in the garbage disposal.</li>
                  <li id="nonMenu">If absolutely nothing happens when you flip the switch, turn off the switch and 
                      push the red reset button on bottom of disposal</li>
                  <li id="nonMenu">If you hear a hum but the motor doesn't spin, turn off the switch.</li>
                  <li id="nonMenu">There is a socket in the center of the bottom of the disposal. Put the hex wrench 
                      (Allen wrench) that came with your disposal or is available at your local hardware 
                      store in that socket, and see if you can free the impeller (thing that spins around).</li>
                  <li id="nonMenu">If you are able to free the impeller but there is still something rattling around in 
                      there, the impeller must be removed.</li>
                  <li id="nonMenu">NEVER put any of your favorite fingers in the garbage disposal.</li>
                  <li id="nonMenu">Cut the top inch off of a yogurt tub (or use a ribbon of any similar plastic), and 
                      stuff it into the rubber opening of the disposal to hold it open.</li>
                  <li id="nonMenu">Use a flashlight and long pliers or tweezers to remove the culprit.</li>
                  <li id="nonMenu">You may need to press the reset button again.</li>
                  <li id="nonMenu">If the dishwasher does not empty fully, check the discharge hose under the sink. 
                      That hose should enter the cabinet, go toward the top of the cabinet (above where it 
                      connects to the drain), and descend to connect to the drain. This prevents 1) a basin 
                      full of water from going into the dishwasher, and 2) water being pumped from the 
                      dishwasher going backwards when the pump stops.</li>
                  <li id="nonMenu">NEVER put any of your favorite fingers in the garbage disposal.</li>
               </ul></dl>
